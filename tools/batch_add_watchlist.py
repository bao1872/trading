def verify_ts_codes(session) -> set:
    """从 stock_pools 验证 ts_code 是否存在，返回有效集合"""
    valid_codes = set()
    for ts_code, _, _ in STOCKS:
        sql = text("SELECT ts_code, name FROM stock_pools WHERE ts_code = :ts_code LIMIT 1")
        result = session.execute(sql, {"ts_code": ts_code}).fetchone()
        if result:
            valid_codes.add(ts_code)
        else:
            logger.warning("ts_code %s 在 stock_pools 中未找到", ts_code)

    logger.info("验证完成：%d/%d 个 ts_code 有效", len(valid_codes), len(STOCKS))
    return valid_codes


def batch_upsert(session, valid_codes: set, dry_run: bool = False) -> int:
    """批量 UPSERT 自选股，仅更新 hype_logic 字段"""
    upsert_sql = text("""
        INSERT INTO stock_watchlist (ts_code, stock_name, is_monitored, hype_logic)
        VALUES (:ts_code, :stock_name, TRUE, :hype_logic)
        ON CONFLICT (ts_code) DO UPDATE SET
            hype_logic = EXCLUDED.hype_logic,
            stock_name = EXCLUDED.stock_name
    """)

    count = 0
    for ts_code, stock_name, hype_logic in STOCKS:
        if ts_code not in valid_codes:
            logger.warning("跳过无效 ts_code: %s (%s)", ts_code, stock_name)
            continue

        if dry_run:
            logger.info("[DRY-RUN] UPSERT %s %s hype_logic=%s", ts_code, stock_name, hype_logic[:30] + "..." if len(hype_logic) > 30 else hype_logic)
            count += 1
            continue

        session.execute(upsert_sql, {
            "ts_code": ts_code,
            "stock_name": stock_name,
            "hype_logic": hype_logic,
        })
        count += 1

    if not dry_run:
        session.commit()

    return count


def verify_results(session) -> None:
    """验证写入结果"""
    sql = text("""
        SELECT ts_code, stock_name, hype_logic
        FROM stock_watchlist
        WHERE ts_code = ANY(:codes)
        ORDER BY ts_code
    """)
    codes = [s[0] for s in STOCKS]
    result = session.execute(sql, {"codes": codes}).fetchall()

    logger.info("=== 验证结果 ===")
    logger.info("共 %d 条记录", len(result))
    for row in result:
        ts_code, stock_name, hype_logic = row
        display = hype_logic[:40] + "..." if hype_logic and len(hype_logic) > 40 else (hype_logic or "(空)")
        logger.info("  %s %s | %s", ts_code, stock_name, display)


def main(dry_run: bool = False, verify_only: bool = False) -> None:
    with get_session() as session:
        if verify_only:
            verify_results(session)
            return

        valid_codes = verify_ts_codes(session)
        count = batch_upsert(session, valid_codes, dry_run=dry_run)
        logger.info("处理完成：%d 条记录", count)

        if not dry_run:
            verify_results(session)


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="批量添加自选股并写入炒作逻辑")
    parser.add_argument("--dry-run", action="store_true", help="仅打印操作，不实际写入")
    parser.add_argument("--verify-only", action="store_true", help="仅验证已有数据")
    args = parser.parse_args()
    main(dry_run=args.dry_run, verify_only=args.verify_only)
